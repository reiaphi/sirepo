            <h6 class="col">
                <p>UU RI No. 12 Tahun 2012 tentang pendidikan tinggi menuliskan bahwa pendidikan tinggi yang bermutu adalah pendidikan tinggi yang mampu menghasilkan lulusan bermutu. Salah satu caranya yaitu dengan memberikan otonomi kepada pendidikan tinggi untuk penyelenggaraan dan pengelolaan kelembagaannya. Meskipun begitu, penyelenggaraan dan pengelolaan pendidikan tinggi harus tetap diarahkan kepada Good University Governance (GUG). Untuk menjamin hal tersebut, maka dapat dibentuk fungsi Satuan Pengawas Internal (SPI). SPI memiliki tugas yang bersifat repetitif dan hanya melakukan tugasnya secara periodik. Hal ini memunculkan defisiensi, karena penindaklanjutan terhadap kesalahan akan terlambat sehingga berdampak pada tidak bermutunya lulusan yang dihasilkan. Untuk mengatasi permasalahan ini dapat digunakan teknologi continuous auditing. Oleh karena itu, penelitian ini bertujuan untuk melihat faktor-faktor yang memengaruhi intensi auditor internal universitas terhadap pengadopsian continuous auditing. Dari penelitian ini maka dapat memberikan gambaran kepada pemangku kepentingan di universitas untuk perencanaan penerapan teknologi continuous auditing. Faktor yang dianalisis didasarkan pada model Unified Theory of Acceptance and Usage of Technology (UTAUT). Pengumpulan data dilakukan pada universitas di Yogyakarta dengan metode purposive sampling. Jumlah data yang diolah adalah sebesar 60 dengan teknik analisis data SEM-PLS. Hasil penelitian menunjukkan bahwa variabel Effort Expectancy, Social Influence, dan Facilitating Conditions memiliki pengaruh positif yang signifikan terhadap intensi pengadopsian continuous auditing. Selain itu, dari hasil pengujian diketahui bahwa variabel moderasi yang memiliki pengaruh signifikan adalah pengalaman terhadap Facilitating Conditions dan kesukarelaan penggunaan terhadap Social Influence.
                </p>
                <p> Republic of Indonesia Higher Education Law Number 12 of 2012 stated a good quality education is the one to produce excellent alumnus. One of the ways is to provide autonomy to higher education in administering and organizing the institution. Even though, the administering and organizing has to lead towards Good University Governance (GUG). To guarantee this, an internal audit unit function can be formed. Internal audit unit has repetitive tasks, but only does its works periodically. This raises deficiencies, as the responding to mistakes will be too late so resulting impact on the unqualified alumnus. To overcome the problem, continuous auditing technology can be implemented. Therefore, this study aims to look at the factors that influence the intention of university internal auditors on the adoption of continuous auditing. This research provide an overview to stakeholders of the university in planning the implementation of continuous auditing. The analyzed factors are based on the Unified Theory of Acceptance and Usage of Technology (UTAUT). Data collection was conducted at universities located in Yogyakarta with a purposive sampling method. The amount of data processed is 60 with SEM-PLS as data analysis techniques. The results shown that the Effort Expectancy, Social Influence, and Facilitating Conditions had a significant positive effect on the intention to adopt continuous auditing. In addition, from the result also known that moderating variables that have significant influence are experience on Facilitating Conditions and voluntariness of use on Social Influence.
                </p>
